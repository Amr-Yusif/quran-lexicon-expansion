def _analyze_disconnected_letters(self) -> List[str]:
    """
    تحليل الحروف المقطعة في أوائل السور
    
    Returns:
        الحروف المقطعة في أوائل السور
    """
    disconnected_letters = []
    surahs = self.quran_data.get("surahs", [])
    
    for surah in surahs:
        first_word = surah.get("verses", [])[0].get("text", "").split()[0]
        if first_word:
            disconnected_letters.append(first_word[0])
    
    return disconnected_letters

